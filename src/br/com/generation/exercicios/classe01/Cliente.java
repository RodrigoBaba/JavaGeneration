package br.com.generation.exercicios.classe01;

public class Cliente {
	
	public String nome;
	public String telefone;
	public String cidade;
		 
	
	public String nomeCliente(String cliente) {
		nome = cliente;
		return cliente;
	}

	public String telefoneCliente(String cliente) {
		telefone = cliente;
		return cliente;
	}

	public String cidadeCliente(String cliente) {
		cidade = cliente;
		return cliente;
	}
	
	
	
	
}
